<strong>&iquest;C&oacute;mo realizar búsquedas?</strong>
<div>Podrá realizar búsquedas mediante dos criterios, el sistema desplegará todas las posibilidades existente.</div>
<div>- Al realizar búsquedas por Patterns (Colores/Estampados), el sistema desplegará todas las tallas disponibles según la disponibilidad de inventario.</div>
<div>- Al realizar búsquedas por Tallas, el sistema desplegará todos los patterns disponibles para la talla requerida.</div>
<br />
<div><strong>&iquest;Qu&eacute; Talla Seleccionar?</strong></div>
<div>Puede consultar su talla en la tabla de equivalencia presionando: <strong>Tabla de Tallas</strong>.</div>

<div>En el Menú Principal en la sección de SERVICIO AL CLIENTE encontrará información relacionada a cómo tomar las medidas corporales.</div>
<br />
<div><strong>&iquest;C&oacute;mo cotizar un producto Genérico?</strong></div>

<div>Si desea cotizar un producto de forma genérica, y no tiene definido que talla o color desea, seleccione lo siguiente:</div>

<div><strong>Talla</strong>: Genérico</div>

<div><strong>Pattern</strong>: "Color" o "Estampado" según corresponda.</div>
